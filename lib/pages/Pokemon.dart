class Pokemon {
  final int id;
  final String name;
  final String types;
  final double height;
  final double weight;
  final String abilities;
  final String stats;
  final String moves;

  Pokemon({
    required this.id,
    required this.name,
    required this.types,
    required this.height,
    required this.weight,
    required this.abilities,
    required this.stats,
    required this.moves,
  });
}
